package org.idsiom.utilbet.bo;

public class CodigosPartidoBO {
	private String nombreLiga;
	
	private Integer codigoFavorito;
	
	private Integer codigoPerdedor;
	
	private Integer codigoEmpate;

	public String getNombreLiga() {
		return nombreLiga;
	}

	public void setNombreLiga(String nombreLiga) {
		this.nombreLiga = nombreLiga;
	}

	public Integer getCodigoFavorito() {
		return codigoFavorito;
	}

	public void setCodigoFavorito(Integer codigoFavorito) {
		this.codigoFavorito = codigoFavorito;
	}

	public Integer getCodigoPerdedor() {
		return codigoPerdedor;
	}

	public void setCodigoPerdedor(Integer codigoPerdedor) {
		this.codigoPerdedor = codigoPerdedor;
	}

	public Integer getCodigoEmpate() {
		return codigoEmpate;
	}

	public void setCodigoEmpate(Integer codigoEmpate) {
		this.codigoEmpate = codigoEmpate;
	}
	
	
}
